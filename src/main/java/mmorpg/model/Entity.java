package mmorpg.model;

import java.util.ArrayList;
import mmorpg.room.Room;

public class Entity {

    private int id;
    private double mass;
    private String name;
    private String description;
    private Room room = new Room();
    ArrayList handles = new ArrayList();

    public void addHandle(String handle) {
        handle = handle.toLowerCase();
        handles.add(handle);
    }

    public boolean hasHandle(String handle) {
        handle = handle.toLowerCase();
        return handles.contains(handle);
    }

    /**
     * @return the mass
     */
    public double getMass() {
        return mass;
    }

    /**
     * @param mass the mass to set
     */
    public void setMass(double mass) {
        this.mass = mass;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * Will not add color tags by default
     *
     * @param addColorTag Whether to surround the name with color code tags
     * @return the name in chosen format public String getName(boolean
     * addColorTag){ return name; }
     */
    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
        addHandle(name);
    }

    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description the description to set
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * @return the room
     */
    public Room getRoom() {
        return room;
    }

    /**
     * @param room the room to set
     */
    public void setRoom(Room room) {
        this.room = room;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }
}
