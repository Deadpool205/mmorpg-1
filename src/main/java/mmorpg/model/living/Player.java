package mmorpg.model.living;

import java.io.PrintWriter;
import java.util.HashMap;
import mmorpg.util.Role;

public class Player extends Living {

    HashMap alias = new HashMap();
    private String userID = "";
    private String password = "";

    private boolean loggedIn = false;
    
    public Player() {

        double m = 100*1000 + ((Math.random() * 25) * 1000);
        super.setMass(m);
        getExperience().setValue(0);
        getHealth().setValue(100);
        getStrength().setValue(10);
        getWisdom().setValue(10);
        getIntelligence().setValue(10);
        getSpeed().setValue(10);
        getEnergy().setValue(10);
        getHunger().setValue(10);
    }

    @Override
    public void think() {
    }

    /**
     * @return the userID
     */
    public String getUserID() {
        return userID;
    }

    /**
     * @param userID the userID to set
     */
    public void setUserID(String userID) {
        this.userID = userID;
    }

    /**
     * @return the password
     */
    public String getPassword() {
        return password;
    }

    /**
     * @param password the password to set
     */
    public void setPassword(String password) {
        this.password = password;
    }

    /**
     * @return the loggedIn
     */
    public boolean isLoggedIn() {
        return loggedIn;
    }

    /**
     * @param loggedIn the loggedIn to set
     */
    public void setLoggedIn(boolean loggedIn) {
        this.loggedIn = loggedIn;
    }
}